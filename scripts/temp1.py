# 检查通道连接是否重复的函数
def check_duplicate_connections(data):
    connection_set = set()
    duplicates = []

    for entry in data:
        channel1, channel2 = entry[0], entry[1]
        # 无方向连接，因此创建一个有序元组
        connection = tuple(sorted([channel1, channel2]))
        
        if connection in connection_set:
            duplicates.append(connection)
        else:
            connection_set.add(connection)
    
    return duplicates

# 检查重复
